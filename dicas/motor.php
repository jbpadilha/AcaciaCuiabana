		<li>O óleo do motor é comparado ao nosso sangue, ou seja, não podemos misturar marcas diferentes, ou produtos químicos (aditivos);</li>
		<li>É importancia que o filtro de óleo seja trocado sempre com troca de óleo;</li>
		<li>Nunca troque a marca do óleo ou complete com óleo de outro fabricante (poderá ocorrer uma reação quimica - borra);</li>
		<li>Caso desejar trocar a marca do óleo, primeiro retire todo óleo do motor e, em seguida, complete com novo de seu escolha, funcine o motor por uma hora e depois retire e complete novamente com óleo de sua preferência.</li>
	</ul>

