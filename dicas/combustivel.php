	<h1>... para economizar combustível, rodando na cidade...</h1>
</div>

<div id="conteudo">
	<ul class="nlista">
		<img class="ilustra" src="dicas/_img/combustivel.jpg" alt="" />
		<li>Procure andar sempre com a rotação do motor baixa.</li>
		<li>Não utilize freqüentemente o veículo com o combustível no limite (no tanque reserva), isso previne danificações no sistema de injeção e também na bomba de combustível e um conseqüente consumo maior. </li>
		<li>Verifique regularmente os pneus. Falta de alinhamento, balanceamento e até mesmo pneus murchos exigem mais do carro, provocando um maior consumo de combustível. </li>
		<li>Use moderadamente o ar condicionado, o mau uso dele aumenta o consumo de combustível. </li>
		<li>Em congestionamentos, ou mesmo em trânsito lento, acelerações fortes e frenagens bruscas provocam o desperdício de combustível. Carros desengatados gastam mais. </li>
		<li>Não sobrecarregue o carro. O transporte de cargas desnecessárias, ou um número excessivo de passageiros, faz com que você gaste mais para alcançar o desempenho desejado. </li>
		<li>Abastecer em postos confiáveis. A qualidade do combustível interfere no rendimento do veículo. </li>
		<li>Utilize corretamente as marchas, para o carro entrar em movimento ele necessita de mais força (a marcha adequada é a primeira), daí por diante use a marcha mais alta possível, sem forçar o motor. </li>
	</ul>
	
	<ul class="ilista">
		<li>Para trocas de marchas, prefira a transmissão manual ao invés da automática.</li>
	</ul>
</div>

